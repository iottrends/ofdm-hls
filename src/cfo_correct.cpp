// ── Fixed-point sin/cos from phase accumulator ─────────────
// phase_acc is ap_uint<32>, full range [0, 2^32) = [0, 2π).
// Returns sin and cos in ap_fixed<16,1> (Q0.15).
static void sincos_lut(ap_uint<32> phase_acc, sample_t &sin_out, sample_t &cos_out) {
    #pragma HLS INLINE
    #pragma HLS PIPELINE II=1

    ap_uint<2> quadrant = phase_acc(31, 30);
    ap_uint<8> addr     = phase_acc(29, 22);
    // For higher accuracy, interpolation fraction from bits [21:14]:
    // ap_uint<8> frac = phase_acc(21, 14);
    // For V1, skip interpolation — 256-entry LUT gives ~14-bit accuracy,
    // sufficient for Q0.15 samples.

    sample_t sin_raw = SIN_LUT[addr];
    sample_t cos_raw = SIN_LUT[(ap_uint<8>)(255 - addr)];

    // Quadrant rotation
    switch (quadrant) {
        case 0:  sin_out =  sin_raw; cos_out =  cos_raw; break;
        case 1:  sin_out =  cos_raw; cos_out = -sin_raw; break;
        case 2:  sin_out = -sin_raw; cos_out = -cos_raw; break;
        default: sin_out = -cos_raw; cos_out =  sin_raw; break; // case 3
    }
}

// ── Top-level CFO correction (fixed-point, drop-in replacement) ──
void cfo_correct(
    hls::stream<iq_t>& iq_in,
    hls::stream<iq_t>& iq_out,
    cfo_t              cfo_est,
    ap_uint<8>         n_syms
)
{
    #pragma HLS INTERFACE axis      port=iq_in
    #pragma HLS INTERFACE axis      port=iq_out
    #pragma HLS INTERFACE s_axilite port=cfo_est bundle=ctrl
    #pragma HLS INTERFACE s_axilite port=n_syms  bundle=ctrl
    #pragma HLS INTERFACE s_axilite port=return  bundle=ctrl

    const int total_samples = ((int)n_syms + 2) * SYNC_NL;

    // CFO deadband (same as v1)
    const float cfo_float  = (float)cfo_est;
    const float cfo_min_sc = 0.01f;
    const float eff_cfo    = (cfo_float > cfo_min_sc || cfo_float < -cfo_min_sc)
                              ? cfo_float : 0.0f;

    // NCO phase step (identical to v1)
    ap_uint<32> delta_fixed = (ap_uint<32>)(int32_t)((double)eff_cfo / (double)FFT_SIZE
                                                     * 4294967296.0);
    ap_uint<32> phase_acc = 0;

    CORRECT: for (int n = 0; n < total_samples; n++) {
#pragma HLS PIPELINE II=1
#pragma HLS loop_tripcount min=SYNC_NL max=((MAX_DATA_SYMS+2)*SYNC_NL)
        iq_t s = iq_in.read();

        phase_acc -= delta_fixed;

        // Fixed-point sin/cos lookup (replaces hls::sincosf)
        sample_t cos_p, sin_p;
        sincos_lut(phase_acc, sin_p, cos_p);

        // Complex rotation (same as v1, but using ap_fixed multiply)
        // 4 multiplies → 4 DSP48 (same count as float, but simpler routing)
        // Actually fewer because ap_fixed<16,1> × ap_fixed<16,1> fits in
        // one DSP48 each (16×16 → 32-bit product, truncate to 16).
        typedef ap_fixed<32, 2> acc_t;
        acc_t in_i = (acc_t)s.i;
        acc_t in_q = (acc_t)s.q;
        acc_t c    = (acc_t)cos_p;
        acc_t sn   = (acc_t)sin_p;

        iq_t out;
        out.i    = sample_t(in_i * c + in_q * sn);
        out.q    = sample_t(-in_i * sn + in_q * c);
        out.last = s.last;
        iq_out.write(out);
    }
}
