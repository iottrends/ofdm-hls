"""
gen_zc_lut.py — Generate Zadoff-Chu preamble LUT for ofdm_tx.cpp

Outputs a C array fragment for ZC_I_LUT and ZC_Q_LUT.
Paste the output into ofdm_tx.cpp to replace the placeholder values.

Usage: python3 gen_zc_lut.py
"""

import numpy as np

# ── Parameters (must match ofdm_tx.h) ──────────────────────────
FFT_SIZE      = 256
ZC_ROOT       = 25          # root u — must be coprime with FFT_SIZE
Q_SCALE       = 2**14 - 1   # Q1.14 max value = 16383

# Null subcarrier indices (must match ofdm_tx.cpp)
NULL_IDX  = set([0, 128] + list(range(1, 25)) + list(range(232, 256)))
PILOT_IDX = {50, 75, 100, 154, 179, 204}
ACTIVE_IDX = [k for k in range(FFT_SIZE) if k not in NULL_IDX]  # 206 total

# ── Generate ZC sequence ────────────────────────────────────────
k  = np.arange(FFT_SIZE)
zc = np.exp(-1j * np.pi * ZC_ROOT * k * (k + 1) / FFT_SIZE)

# Select active subcarriers only (pilots first, then data — match LUT order)
# In ofdm_tx.cpp the pilot loop uses UNROLL and data loop uses the DATA_SC_IDX.
# The ZC LUT is indexed by d (data index 0..199), pilots are hardcoded separately.
# Here we output data subcarriers in DATA_SC_IDX order.

DATA_SC_IDX = (
    list(range(25, 50)) + list(range(51, 75)) +
    list(range(76, 100)) + list(range(101, 128)) +
    list(range(129, 154)) + list(range(155, 179)) +
    list(range(180, 204)) + list(range(205, 232))
)
assert len(DATA_SC_IDX) == 200, f"Expected 200, got {len(DATA_SC_IDX)}"

# ── Scale to Q1.14 ──────────────────────────────────────────────
def to_q015(x):
    """Clamp and convert float to ap_fixed<16,1> string literal.
    ap_fixed<16,1>: Q0.15 format, range [-1, +1), resolution 2^-15 = 3.05e-5.
    +1.0 cannot be represented — wraps to -1.0. Must clamp to max = 1 - 2^-15.
    """
    Q_MAX = 1.0 - 2**-15   # = 0.999969482421875
    Q_MIN = -1.0
    clamped = max(Q_MIN, min(Q_MAX, float(x)))
    return f"sample_t({clamped:.6f})"

# ── Print C arrays ──────────────────────────────────────────────
print("// ── ZC Preamble LUT (auto-generated by gen_zc_lut.py) ──")
print(f"// ZC root u={ZC_ROOT}, N={FFT_SIZE}, {len(DATA_SC_IDX)} data subcarriers")
print()

print(f"static const sample_t ZC_I_LUT[NUM_DATA_SC] = {{")
for chunk_start in range(0, len(DATA_SC_IDX), 8):
    chunk = DATA_SC_IDX[chunk_start:chunk_start+8]
    vals  = ", ".join(to_q015(zc[k].real) for k in chunk)
    print(f"    {vals},")
print("};")
print()

print(f"static const sample_t ZC_Q_LUT[NUM_DATA_SC] = {{")
for chunk_start in range(0, len(DATA_SC_IDX), 8):
    chunk = DATA_SC_IDX[chunk_start:chunk_start+8]
    vals  = ", ".join(to_q015(zc[k].imag) for k in chunk)
    print(f"    {vals},")
print("};")

# ── Sanity check ────────────────────────────────────────────────
print()
print("// Sanity: all ZC magnitudes should be 1.0")
mags = np.abs(zc[DATA_SC_IDX])
print(f"// min={mags.min():.6f}  max={mags.max():.6f}  (should both be 1.0)")
