"""
pluto_bist_ofdm_test.py — minimal: TX one OFDM burst, capture via BIST,
undo the 1/16 scale (deterministic, no percentile), feed to decode_full.

Compares to digital-mode reference: if BIST + ×16 == digital, BER should be 0.
Run: python3 sim/pluto_bist_ofdm_test.py
"""
import time
import numpy as np
import adi
import ofdm_reference as ofdm

# ── Generate one OFDM frame (writes tb_input_to_tx.bin for ground truth) ──
print("[test] generating OFDM burst...")
sig = ofdm.generate(mod=1, n_syms=255, fec_rate=0)   # 16QAM 1/2, 255 syms
sig = sig.astype(np.complex64)

# Pad with zeros so cyclic TX has clean boundaries between repeats
pad = 2 * (ofdm.FFT_SIZE + ofdm.CP_LEN)              # 576 samples each side
burst = np.concatenate([np.zeros(pad, dtype=np.complex64),
                         sig,
                         np.zeros(pad, dtype=np.complex64)])
N_burst = len(burst)
print(f"[test] burst len = {N_burst} samples ({N_burst/20e6*1e3:.2f} ms at 20 MSPS)")

# Scale to int16 with peak 2^13 (12 dB headroom for OFDM PAPR)
peak = 2**13
m = np.max(np.abs(burst))
tx_iq = burst * (peak / m) if m > 0 else burst
print(f"[test] TX peak |x| = {np.max(np.abs(tx_iq)):.0f}")

# ── Pluto BIST loopback=1 ──
sdr = adi.Pluto(uri="ip:192.168.2.1")
sdr.sample_rate = 20_000_000
sdr.tx_lo = sdr.rx_lo = 2_400_000_000
sdr.tx_rf_bandwidth = sdr.rx_rf_bandwidth = 25_000_000
sdr.tx_hardwaregain_chan0 = 0.0
sdr.gain_control_mode_chan0 = "manual"
sdr.rx_hardwaregain_chan0 = 20.0
sdr.tx_cyclic_buffer = True
sdr.rx_buffer_size = N_burst * 2 + 4000      # capture > 2 full cycles
sdr._ctrl.debug_attrs["loopback"].value = "1"

sdr.tx(tx_iq)
time.sleep(0.05)
for _ in range(2): sdr.rx()                    # drain stale
rx = np.asarray(sdr.rx(), dtype=np.complex128)
sdr.tx_destroy_buffer()
print(f"[test] RX captured {len(rx)} samples  peak |rx| = {np.max(np.abs(rx)):.0f}")

# ── Cross-correlate to find the first complete burst in RX ──
template = tx_iq[:5000]
corr = np.abs(np.correlate(rx, template, mode="valid"))
offset = int(np.argmax(corr))
print(f"[test] cross-corr alignment: offset = {offset}  peak = {corr[offset]:.3e}")

# Slice exactly one cycle of burst from RX, starting at correlation peak
rx_one = rx[offset : offset + N_burst]
if len(rx_one) < N_burst:
    print(f"[test] WARN: short slice ({len(rx_one)}/{N_burst})")
    # Wrap from beginning to fill
    rx_one = np.concatenate([rx_one, rx[: N_burst - len(rx_one)]])

# ── Undo BIST 1/16 attenuation deterministically ──
rx_scaled = rx_one * (16.0 / peak)             # × 16 (BIST) ÷ 8192 (DAC peak) = back to float ~[-1, 1]
print(f"[test] after × 16/peak: peak |rx| = {np.max(np.abs(rx_scaled)):.4f}  "
      f"(should be ≈1.0)")

# Strip the lead/trail zero pad to match decode_full's expected layout
# (decode_full expects the burst as generated by generate())
rx_unpadded = rx_scaled[pad : pad + len(sig)]
print(f"[test] passing {len(rx_unpadded)} samples to decode_full "
      f"(matches Python TX output length)")

# ── Decode ──
print("[test] decoding...")
res = ofdm.decode_full(
    tx_file=rx_unpadded,
    mod=1, n_syms=255, fec_rate=0,
    use_sync=True,
    use_q15=True,
    use_soft=True,
    use_reed_solomon=True,
    use_cfo_correct=True,
)

if res is None:
    print("[test] decode failed (no result)")
elif res.get("sync_fail"):
    print("[test] FAIL: sync_detect did not trigger")
else:
    bit_err = res.get("bit_errors", 0)
    total = res.get("total_bits", 0)
    ber = bit_err / total if total else float("nan")
    print(f"[test] header_pass={res.get('header_pass')}  "
          f"bit_err={bit_err}/{total}  BER={ber:.2e}")
    if bit_err == 0:
        print("[test] ✅ BIST OFDM ROUND-TRIP CLEAN")
    else:
        print("[test] ❌ residual bit errors after BIST + scale-fix")
